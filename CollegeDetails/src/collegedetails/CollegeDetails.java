    public ArrayList<User> userlist()
    {
       ArrayList<User> userlist = new ArrayList<>();
    try{
      Class.forName("com.mysql.jdbc.Driver");
      Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/studentrecords","root","Krishna@1802");    
      String query1 = "SELECT * FROM studentrec";
      Statement st = con.createStatement();
      ResultSet rs = st.executeQuery(query1);
      User user;
      //System.out.println(rs.getString("subject"));
      while(rs.next()){
          user = new User(rs.getString("name"),rs.getString("address"),rs.getString("gender"),rs.getString("knowledge"),rs.getString("subject"));
          userlist.add(user);
      }
     }
    catch(Exception e){
        JOptionPane.showMessageDialog(null, e);
    }
    return userlist;
    }
    public void show_user(){
        ArrayList<User> list = userlist();
        DefaultTableModel model = (DefaultTableModel)jTable_Display_user.getModel();
        Object[] row = new Object[5];
        for(int i=0;i<list.size();i++)
        {
            row[0] = list.get(i).getname();
            row[1] = list.get(i).getaddress();
            row[2] = list.get(i).getgender();
            row[3] = list.get(i).getknowledge();
            row[4] = list.get(i).getsubject();
            model.addRow(row);
        }
    }
    public void updatetable(String name,String address,String gender,String knowledge,String course){
        DefaultTableModel model = (DefaultTableModel)jTable_Display_user.getModel();
        Object[] row = new Object[5];
        row[0] = name;
        row[1] = address;
        row[2] = gender;
        row[3] = knowledge;
        row[4] = course;
        model.addRow(row);
    }
