    private void savebuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savebuttonActionPerformed
    try{
      Class.forName("com.mysql.jdbc.Driver");
      Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/studentrecords","root","Krishna@1802");    
      String query = "insert into studentrec(name, address, gender, knowledge, subject)values(?,?,?,?,?)";
      PreparedStatement pst = con.prepareStatement(query);
      pst.setString(1, nametext.getText());
      pst.setString(2, addtext.getText());
      String name = nametext.getText();
      String address = addtext.getText();
      if(maleradio.isSelected())
      {
          gender = "Male";
      }
      if(femaleradio.isSelected())
      {
          gender = "Female";
      }
      if(othersradio.isSelected()){
                gender="Others";
      }
      pst.setString(3,gender);
      String knowledge = "";
      if(javaradio.isSelected())
      {
          knowledge += javaradio.getText()+"";
      }
      if(pythonradio.isSelected())
      {
          knowledge += pythonradio.getText()+"";
      }
      if(cppradio.isSelected())
      {
          knowledge+=cppradio.getText()+" ";
      }
      pst.setString(4, knowledge);
      String course;
      course = subjectcombo.getSelectedItem().toString();
      pst.setString(5,course);
      pst.executeUpdate();
      JOptionPane.showMessageDialog(null, "Inserted Sucessfully!");
      updatetable(name,address,gender,knowledge,course);
      resetaction();
    }
    
     catch(Exception e){
      JOptionPane.showMessageDialog(null, e);
        }
                                    
    }//GEN-LAST:event_savebuttonActionPerformed

    private void deletebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletebtnActionPerformed
        // TODO add your handling code here:
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/studentrecords","root","Krishna@1802");    
            int row = jTable_Display_user.getSelectedRow();
            String value=(jTable_Display_user.getModel().getValueAt(row, 0).toString());
            String query="DELETE FROM studentrec where name=\""+value+"\"";
            PreparedStatement pst = con.prepareStatement(query);
            pst.executeUpdate();
            DefaultTableModel model = (DefaultTableModel)jTable_Display_user.getModel();
            model.setRowCount(0);
            show_user();
            JOptionPane.showMessageDialog(null,"Deleted Successfully!");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
 
    }//GEN-LAST:event_deletebtnActionPerformed

